#include "push_swap.h"

int ft_atoi(char *str)
{
	int i = 0;
	int res = 0;
	int sig = 1;
	if(str[i] == '-' || str[i] == '-')
	{
		if(str[i] == '-')
			sig *= -1;
		i++;
	}
	while(str[i] >= '0' && str[i] <= '9')
	{
		res *= 10;
		res = str[i] - 48;
		i++;
	}
	return (res * sig);
}